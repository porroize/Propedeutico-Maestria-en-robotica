public class Decremento{
	public static void main(String [] args){
		int b= 5;
		System.out.println(b);
		System.out.println(b--);
		System.out.println(b);

		b = 5;
		System.out.println(b);
		System.out.println(--b);
		System.out.println(b);

	}
}